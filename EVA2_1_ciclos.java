public class EVA2_1_ciclos {

    public static void main(String[] args) {
        //System.out.println("Hello World!");
        //For (desde) cuando sabes cuantas veces se va a repetir la instruccion
        //While (mientras) cuando se repite 0 o n veces
        //Do While (Hacer mientras) Cuando se repite almenos una vez hasta n veces 
        
        //Ciclo for
        //(++) Suma una unidad
        //(--) Resta una unidad
        //(+=) Suma n Valor
        //(-=9 Resta n valor
        for(int i = 1; i <= 10; i++){
            System.out.print(i+"-");  
        }
        System.out.println("");
        for(int i = 10; i >= 1; i--){
            System.out.print(i+"-");  
        }
        
        
        
        
    }
}
